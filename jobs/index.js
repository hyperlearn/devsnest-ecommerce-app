const cron = require('node-cron');

/**
 * 
 # ┌────────────── second (optional)
 # │ ┌──────────── minute
 # │ │ ┌────────── hour
 # │ │ │ ┌──────── day of month
 # │ │ │ │ ┌────── month
 # │ │ │ │ │ ┌──── day of week
 # │ │ │ │ │ │
 # │ │ │ │ │ │
 # * * * * * *

second	0-59
minute	0-59
hour	0-23
day of month	1-31
month	1-12 (or names)
day of week	0-7 (or names, 0 or 7 are sunday)
 */


const scheduleJobs = () => {
  cron.schedule('5 * * * *', () => {
    console.log('In every 5th second of every minute');
  });
}


module.exports = scheduleJobs;